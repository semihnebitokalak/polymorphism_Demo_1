public class Calculator {
    // İki tam sayiyi toplama metodu
    public int add(int x, int y){
        return x + y;
    }
    // üç tam sayiyi toplama metodu
    public int add(int x, int y, int z){
        return x + y + z;
    }
    // İki ondalıklı sayıyı toplayan metod
    public double add(double a, double b) {
        return a + b;
    }
}
